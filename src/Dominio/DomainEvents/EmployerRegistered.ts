class EmployeRegistered {
  public Name: String;
  public Description: String;
  public Location: String;
  public Rif: String;
  public Phone: String;
  public Mail: String;
  public ComDesignation: String;

  constructor(
    Name: String,
    Description: String,
    Location: String,
    Rif: String,
    Phone: String,
    Mail: String,
    ComDesignation: String,
  ) {
    this.Name = Name;
    this.Description = Description;
    this.Location = Location;
    this.Rif = Rif;
    this.Phone = Phone;
    this.Mail = Mail;
    this.ComDesignation = ComDesignation;
  }
}
